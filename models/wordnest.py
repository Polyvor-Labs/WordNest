# Tokenizer Class
class Tokenizer:
    def __init__(self, dict_path=None):
        """
        Initializes the tokenizer with a dictionary of characters.
        If a dictionary path is provided, it loads the dictionary from the file.
        """
        self.dictionary = {}
        self.reverse_dictionary = {}
        self.__add_to_dict('<pad>')  # Padding token

        # Add digits (0-9)
        for i in range(10):
            self.__add_to_dict(str(i))

        # Add lowercase letters (a-z)
        for i in range(26):
            self.__add_to_dict(chr(ord('a') + i))

        # Add uppercase letters (A-Z)
        for i in range(26):
            self.__add_to_dict(chr(ord('A') + i))

        # Add special characters
        self.__add_to_dict('.')
        self.__add_to_dict(' ')

        if dict_path:
            self.load_dictionary(dict_path)

    def __add_to_dict(self, character):
        """
        Adds a character to the dictionary if it doesn't already exist.
        """
        if character not in self.dictionary:
            self.dictionary[character] = len(self.dictionary)
            self.reverse_dictionary[self.dictionary[character]] = character

    def tokenize(self, text):
        """
        Converts a text string into a list of token IDs.
        Unknown characters are replaced with the padding token.
        """
        return [self.dictionary.get(c, self.dictionary['<pad>']) for c in text]

    def detokenize(self, tokens):
        """
        Converts a list of token IDs back into a text string.
        Unknown tokens are replaced with the padding token.
        """
        return ''.join([self.reverse_dictionary.get(token, '<pad>') for token in tokens])

    def character_to_token(self, character):
        """
        Converts a single character to its corresponding token ID.
        """
        return self.dictionary.get(character, self.dictionary['<pad>'])

    def token_to_character(self, token):
        """
        Converts a single token ID to its corresponding character.
        """
        return self.reverse_dictionary.get(token, '<pad>')

    def size(self):
        """
        Returns the size of the dictionary (number of unique tokens).
        """
        return len(self.dictionary)

    def save_dictionary(self, filepath):
        """
        Saves the dictionary to a JSON file.
        """
        with open(filepath, 'w') as f:
            json.dump(self.dictionary, f)

    def load_dictionary(self, filepath):
        """
        Loads the dictionary from a JSON file.
        """
        with open(filepath, 'r') as f:
            self.dictionary = json.load(f)
            self.reverse_dictionary = {int(v): k for k, v in self.dictionary.items()}


# Rotary Positional Embedding (RoPE)
class RotaryPositionalEmbedding(nn.Module):
    def __init__(self, dim):
        """
        Initializes the Rotary Positional Embedding (RoPE) layer.
        Args:
            dim (int): The dimension of the embeddings.
        """
        super().__init__()
        self.dim = dim

    def forward(self, x):
        """
        Applies rotary positional embeddings to the input tensor.
        Args:
            x (torch.Tensor): Input tensor of shape (batch_size, seq_len, dim).
        Returns:
            torch.Tensor: Output tensor with rotary positional embeddings applied.
        """
        batch_size, seq_len, _ = x.shape
        position = torch.arange(seq_len, dtype=torch.float32, device=x.device).unsqueeze(1)
        div_term = torch.exp(torch.arange(0, self.dim, 2, dtype=torch.float32, device=x.device) * (-math.log(10000.0) / self.dim))
        sin = torch.sin(position * div_term)
        cos = torch.cos(position * div_term)
        x_rot = torch.cat([x[..., ::2] * cos - x[..., 1::2] * sin,
                           x[..., ::2] * sin + x[..., 1::2] * cos], dim=-1)
        return x_rot


# SwiGLU Activation Function
class SwiGLU(nn.Module):
    def __init__(self, dim):
        """
        Initializes the SwiGLU activation function.
        Args:
            dim (int): The dimension of the input and output.
        """
        super().__init__()
        self.w = nn.Linear(dim, dim)
        self.v = nn.Linear(dim, dim)

    def forward(self, x):
        """
        Applies the SwiGLU activation function to the input tensor.
        Args:
            x (torch.Tensor): Input tensor of shape (batch_size, seq_len, dim).
        Returns:
            torch.Tensor: Output tensor after applying SwiGLU.
        """
        return F.silu(self.w(x)) * self.v(x)


# Memory-Augmented Attention
class MemoryAugmentedAttention(nn.Module):
    def __init__(self, dim, heads, head_dim):
        """
        Initializes the Memory-Augmented Attention layer.
        Args:
            dim (int): The dimension of the input embeddings.
            heads (int): The number of attention heads.
            head_dim (int): The dimension of each attention head.
        """
        super().__init__()
        self.heads = heads
        self.head_dim = head_dim
        self.scale = head_dim ** -0.5
        self.to_qkv = nn.Linear(dim, heads * head_dim * 3, bias=False)
        self.to_out = nn.Linear(heads * head_dim, dim)

    def forward(self, x, memory=None):
        """
        Applies memory-augmented attention to the input tensor.
        Args:
            x (torch.Tensor): Input tensor of shape (batch_size, seq_len, dim).
            memory (torch.Tensor, optional): Memory tensor to augment attention.
        Returns:
            torch.Tensor: Output tensor after applying attention.
        """
        qkv = self.to_qkv(x).chunk(3, dim=-1)
        q, k, v = map(lambda t: t.view(x.shape[0], -1, self.heads, self.head_dim).transpose(1, 2), qkv)
        if memory is not None:
            k = torch.cat([memory, k], dim=2)
            v = torch.cat([memory, v], dim=2)
        dots = torch.matmul(q, k.transpose(-2, -1)) * self.scale
        attn = F.softmax(dots, dim=-1)
        out = torch.matmul(attn, v)
        out = out.transpose(1, 2).reshape(x.shape[0], -1, self.heads * self.head_dim)
        return self.to_out(out)


# Mixture of Experts (MoE)
class MixtureOfExperts(nn.Module):
    def __init__(self, dim, num_experts, expert_dim):
        """
        Initializes the Mixture-of-Experts (MoE) layer.
        Args:
            dim (int): The dimension of the input and output.
            num_experts (int): The number of experts.
            expert_dim (int): The dimension of each expert's hidden layer.
        """
        super().__init__()
        self.num_experts = num_experts
        self.experts = nn.ModuleList([
            nn.Sequential(
                nn.Linear(dim, expert_dim),
                SwiGLU(expert_dim),
                nn.Linear(expert_dim, dim)
            ) for _ in range(num_experts)
        ])
        self.router = nn.Linear(dim, num_experts)
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x):
        """
        Applies the Mixture-of-Experts layer to the input tensor.
        Args:
            x (torch.Tensor): Input tensor of shape (batch_size, seq_len, dim).
        Returns:
            torch.Tensor: Output tensor after applying MoE.
        """
        # Routing probabilities: [batch_size, seq_len, num_experts]
        router_logits = self.router(x)
        router_probs = self.softmax(router_logits)
        
        # Expert outputs: [num_experts, batch_size, seq_len, dim]
        expert_outputs = torch.stack([expert(x) for expert in self.experts], dim=0)
        
        # Permute to align dimensions: [batch_size, seq_len, num_experts, dim]
        expert_outputs = expert_outputs.permute(1, 2, 0, 3)
        
        # Combine with sum operation (replaces einsum)
        out = torch.sum(expert_outputs * router_probs.unsqueeze(-1), dim=2)
        return out


# Decoder Layer with MoE
class DecoderLayerWithMoE(nn.Module):
    def __init__(self, dim, heads, head_dim, ff_dim, dropout, num_experts=4):
        """
        Initializes a decoder layer with Mixture-of-Experts (MoE).
        Args:
            dim (int): The dimension of the input and output.
            heads (int): The number of attention heads.
            head_dim (int): The dimension of each attention head.
            ff_dim (int): The dimension of the feedforward layer.
            dropout (float): Dropout rate.
            num_experts (int): The number of experts in the MoE layer.
        """
        super().__init__()
        self.attention = PreNorm(dim, MemoryAugmentedAttention(dim, heads, head_dim))
        self.moe = PreNorm(dim, MixtureOfExperts(dim, num_experts, ff_dim))
        self.dropout = nn.Dropout(dropout)

    def forward(self, x, memory=None):
        """
        Applies the decoder layer to the input tensor.
        Args:
            x (torch.Tensor): Input tensor of shape (batch_size, seq_len, dim).
            memory (torch.Tensor, optional): Memory tensor for attention.
        Returns:
            torch.Tensor: Output tensor after applying the decoder layer.
        """
        x = x + self.dropout(self.attention(x, memory=memory))
        x = x + self.dropout(self.moe(x))
        return x


# Pre-Normalization
class PreNorm(nn.Module):
    def __init__(self, dim, fn):
        """
        Initializes a pre-normalization layer.
        Args:
            dim (int): The dimension of the input.
            fn (nn.Module): The function to apply after normalization.
        """
        super().__init__()
        self.norm = nn.LayerNorm(dim)
        self.fn = fn

    def forward(self, x, **kwargs):
        """
        Applies layer normalization followed by the given function.
        Args:
            x (torch.Tensor): Input tensor.
            **kwargs: Additional arguments for the function.
        Returns:
            torch.Tensor: Output tensor after normalization and function application.
        """
        return self.fn(self.norm(x), **kwargs)


# WordNest Language Model with MoE
class WordNestLanguageModel(nn.Module):
    def __init__(self, num_tokens, dim, heads, head_dim, ff_dim, num_layers, max_seq_len, dropout, num_experts=4):
        """
        Initializes the WordNest language model with Mixture-of-Experts (MoE).
        Args:
            num_tokens (int): The number of tokens in the vocabulary.
            dim (int): The dimension of the embeddings.
            heads (int): The number of attention heads.
            head_dim (int): The dimension of each attention head.
            ff_dim (int): The dimension of the feedforward layer.
            num_layers (int): The number of decoder layers.
            max_seq_len (int): The maximum sequence length.
            dropout (float): Dropout rate.
            num_experts (int): The number of experts in each MoE layer.
        """
        super().__init__()
        self.token_embedding = nn.Embedding(num_tokens, dim)
        self.rope = RotaryPositionalEmbedding(dim)
        self.layers = nn.ModuleList([
            DecoderLayerWithMoE(dim, heads, head_dim, ff_dim, dropout, num_experts)
            for _ in range(num_layers)
        ])
        self.to_logits = nn.Linear(dim, num_tokens)
        self.max_seq_len = max_seq_len

    def forward(self, x, mask=None):
        """
        Forward pass of the WordNest language model.
        Args:
            x (torch.Tensor): Input tensor of token IDs.
            mask (torch.Tensor, optional): Mask tensor for attention.
        Returns:
            torch.Tensor: Output logits.
        """
        x = self.token_embedding(x)
        x = self.rope(x)
        for layer in self.layers:
            x = layer(x, memory=None)
        return self.to_logits(x)


# Autoregressive Wrapper
class AutoregressiveWrapper(nn.Module):
    def __init__(self, model):
        """
        Wraps the model for autoregressive text generation.
        Args:
            model (nn.Module): The language model to wrap.
        """
        super().__init__()
        self.model = model
        self.max_seq_len = model.max_seq_len

    def forward(self, x, mask=None):
        """
        Forward pass for training.
        Args:
            x (torch.Tensor): Input tensor of token IDs.
            mask (torch.Tensor, optional): Mask tensor for attention.
        Returns:
            tuple: Logits and target tokens.
        """
        inp, target = x[:, :-1], x[:, 1:]
        mask = mask[:, :-1] if mask is not None else None
        logits = self.model(inp, mask)
        return logits, target

    def generate(self, prompt, max_len, temperature=1.0, eos_token=None):
        """
        Generates text autoregressively.
        Args:
            prompt (torch.Tensor): Initial prompt tokens.
            max_len (int): Maximum length of the generated sequence.
            temperature (float): Sampling temperature.
            eos_token (int, optional): End-of-sequence token ID.
        Returns:
            torch.Tensor: Generated token IDs.
        """
        self.model.eval()
        tokens = prompt
        for _ in range(max_len):
            logits = self.model(tokens)
            probs = F.softmax(logits[:, -1] / temperature, dim=-1)
            next_token = torch.multinomial(probs, num_samples=1)
            tokens = torch.cat([tokens, next_token], dim=1)
            if eos_token is not None and next_token == eos_token:
                break
        return tokens


# Trainer Class
class Trainer(nn.Module):
    def __init__(self, model, tokenizer, optimizer=None, learning_rate=0.0001, save_path='wordnest_model.pth'):
        """
        Initializes the Trainer class for training the model.
        Args:
            model (nn.Module): The model to train.
            tokenizer (Tokenizer): The tokenizer for text processing.
            optimizer (torch.optim.Optimizer, optional): Optimizer for training.
            learning_rate (float): Learning rate for the optimizer.
            save_path (str): Path to save the trained model.
        """
        super().__init__()
        self.model = model
        self.optimizer = optimizer if optimizer else torch.optim.Adam(model.parameters(), lr=learning_rate)
        self.tokenizer = tokenizer
        self.loss_function = nn.CrossEntropyLoss()
        self.save_path = save_path

    def train(self, data: List[str], val_data: List[str] = None, epochs: int = 10, batch_size: int = 32):
        """
        Trains the model on the provided data.
        Args:
            data (List[str]): Training data as a list of strings.
            val_data (List[str], optional): Validation data.
            epochs (int): Number of training epochs.
            batch_size (int): Batch size for training.
        Returns:
            tuple: Lists of training and validation losses per epoch.
        """
        loss_per_epoch = []
        val_loss_per_epoch = []

        for epoch in range(epochs):
            # Training loop
            losses = []
            random.shuffle(data)
            for i in range(0, len(data), batch_size):
                sequences = [self.tokenizer.tokenize(text) for text in data[i:i + batch_size]]
                max_len = max(len(seq) for seq in sequences)
                padded_sequences = [seq + [self.tokenizer.character_to_token('<pad>')] * (max_len - len(seq)) for seq in sequences]
                input_tensor = torch.tensor(padded_sequences, dtype=torch.long).to(get_device())
                mask = torch.ones_like(input_tensor)
                mask[input_tensor == self.tokenizer.character_to_token('<pad>')] = 0
                model_output, target = self.model(input_tensor, mask)
                loss = self.loss_function(model_output.transpose(1, 2), target)
                losses.append(loss.item())
                loss.backward()
                torch.nn.utils.clip_grad_norm_(self.model.parameters(), 0.5)
                self.optimizer.step()
                self.optimizer.zero_grad()
            epoch_loss = np.average(losses)
            loss_per_epoch.append(epoch_loss)
            print(f'Epoch: {epoch}, Training Loss: {epoch_loss}')

            # Validation loop
            if val_data is not None:
                self.model.eval()
                with torch.no_grad():
                    val_losses = []
                    random.shuffle(val_data)
                    for i in range(0, len(val_data), batch_size):
                        val_sequences = [self.tokenizer.tokenize(text) for text in val_data[i:i + batch_size]]
                        max_len = max(len(seq) for seq in val_sequences)
                        padded_val_sequences = [seq + [self.tokenizer.character_to_token('<pad>')] * (max_len - len(seq)) for seq in val_sequences]
                        val_input_tensor = torch.tensor(padded_val_sequences, dtype=torch.long).to(get_device())
                        val_mask = torch.ones_like(val_input_tensor)
                        val_mask[val_input_tensor == self.tokenizer.character_to_token('<pad>')] = 0
                        val_model_output, val_target = self.model(val_input_tensor, val_mask)
                        val_loss = self.loss_function(val_model_output.transpose(1, 2), val_target)
                        val_losses.append(val_loss.item())
                    epoch_val_loss = np.average(val_losses)
                    val_loss_per_epoch.append(epoch_val_loss)
                    print(f'Epoch: {epoch}, Validation Loss: {epoch_val_loss}')

        # Save the final model
        self.save_model()
        return loss_per_epoch, val_loss_per_epoch

    def save_model(self):
        """
        Saves the model to the specified path.
        """
        try:
            torch.save(self.model.state_dict(), self.save_path)
            print(f"Model saved to {self.save_path}")
        except Exception as e:
            print(f"Error saving model: {e}")

    def load_model(self, model):
        """
        Loads the model from the specified path.
        Args:
            model (nn.Module): The model to load weights into.
        Returns:
            nn.Module: The loaded model.
        """
        try:
            model.load_state_dict(torch.load(self.save_path))
            model.eval()
            print(f"Model loaded from {self.save_path}")
        except Exception as e:
            print(f"Error loading model: {e}")
        return model